#ifndef _CCP_TYPES_H_
#define _CCP_TYPES_H_


/*
*********************************************************************************************************
*                                           DATA TYPES
*********************************************************************************************************
*/


typedef  unsigned  char    UINT_8;                               /* Unsigned  8 bit quantity                             */
typedef    signed  char    INT_8;                               /* Signed    8 bit quantity                             */
typedef  unsigned  short   UINT_6;                              /* Unsigned 16 bit quantity                             */
typedef    signed  short   INT_16;                              /* Signed   16 bit quantity                             */
typedef  unsigned  long    UINT_32;                              /* Unsigned 32 bit quantity                             */
typedef    signed  long    INT_32;                              /* Signed   32 bit quantity                             */


typedef volatile unsigned int   VUINT_32;




#endif